public class B1_typeCasting {
    public static void main(String[] args) {
        float a =10.5f;
        int b = (int)a;
        System.out.println(b);

        char ch ='a';
        char ch2 ='b';
        int number = ch;
        int num = ch2;
        System.out.println(number);
        System.out.println(num);
        int $ = 24;
        System.out.println($);
    }
}
